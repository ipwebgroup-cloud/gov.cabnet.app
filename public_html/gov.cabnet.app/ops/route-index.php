function routeidx_h(mixed $value): string
{
    return htmlspecialchars((string)$value, ENT_QUOTES | ENT_SUBSTITUTE, 'UTF-8');
}

function routeidx_url(string $path): string
{
    $path = str_replace('\\', '/', $path);
    $prefix = 'public_html/gov.cabnet.app';
    if (str_starts_with($path, $prefix)) {
        $url = substr($path, strlen($prefix));
        return $url !== '' ? $url : '/';
    }
    return '';
}

function routeidx_badge(string $text, string $type = 'neutral'): string
{
    return opsui_badge($text, $type);
}

function routeidx_rec_type(string $rec): string
{
    return match ($rec) {
        'KEEP' => 'good',
        'KEEP ADMIN/AUDIT' => 'info',
        'KEEP INTERNAL' => 'dark',
        'KEEP SEPARATE' => 'warn',
        'LOCKED REVIEW' => 'warn',
        'HIDE/ARCHIVE' => 'neutral',
        default => 'neutral',
    };
}

